public class pro4 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Equal to operator
        boolean c = (a == b);
        System.out.println("a == b is " + c);

        // Not equal to operator
        c = (a != b);
        System.out.println("a ! = b is " + c);

        // Less than operator
        c = (a < b);
        System.out.println("a < b is " + c);

        //Greater  operator
        c = (a > b);
        System.out.println("a > b is " + c);

    }
}
